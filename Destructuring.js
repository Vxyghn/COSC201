//Example of destructuring An Object
//Example 1
const animal = { name: 'Whale', weight: 450  };
const { name, weight } = animal;

console.log(name); 
console.log(weight); 

//Example 2
//Destructuring An Array
const numbers = [1, 2, 3];
const [first, second] = numbers;

console.log(first);  
console.log(second); 
